class CNNLstm(BaseFeaturesExtractor):
    """
    The main idea behind of this architecture is to combine CNN feature extractor
    and LSTM together. CNN will extract features of each frame and LSTM uses these
    frames features (stacked frames) and use it as an semi-time series input
    
    :param observation_space: (gym.Space)
    :param features_dim: (int) Number of features extracted.
        This corresponds to the number of unit for the last layer.
    """

    def __init__(self, observation_space: spaces.Box, features_dim: int = 512):
        super().__init__(observation_space, features_dim)
        # We assume CxHxW images (channels first)
        # Re-ordering will be done by pre-preprocessing or wrapper
        n_input_channels = observation_space.shape[0]
        self.n_frames = int(n_input_channels/3)
        self.cnn = nn.Sequential(
            nn.Conv2d(3, 32, kernel_size=8, stride=4, padding=0),
            nn.ReLU(),
            nn.Conv2d(32, 64, kernel_size=4, stride=2, padding=0),
            nn.ReLU(),
            nn.Conv2d(64, 64, kernel_size=3, stride=1, padding=0),
            nn.ReLU(),
            nn.Flatten(),
        )

        # Compute shape by doing one forward pass
        with th.no_grad():
            n_flatten = self.cnn(
                th.as_tensor(observation_space.sample()[None]).float()
            ).shape[1]
        
        features_lstm = (self.n_frames*n_flatten)/2

        '''
        TODO:
            shapes should be calculated.
        '''
        self.lstm = nn.Sequential(nn.LSTM(self.n_frames, n_flatten, features_lstm), nn.ReLU())
        self.linear = nn.Sequential(nn.Linear(n_flatten, features_dim), nn.ReLU())

    def forward(self, observations: th.Tensor) -> th.Tensor:
        stacked_cnn = []
        for frame in range(self.n_frames):
            stacked_cnn.append(self.cnn(observations[frame:frame+3,:,:]))
        '''
        TODO:
            implementing stacked output
        '''
        
        stacked_output = th.stack(stacked_cnn)
        self.linear(self.lstm(stacked_output))
        return 

